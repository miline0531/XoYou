package kr.co.genericit.mybase.xoyou2.model;


public class MongDateList {
    private String No;
    private String Info;
    private String DateFormat;
    private String Dou;
    private String HyeonSil;
    private String sDate;
    private String eDate;
    private String Color;
    private String HyeonSilABC;

    public String getNo() {
        return No;
    }

    public void setNo(String no) {
        No = no;
    }

    public String getInfo() {
        return Info;
    }

    public void setInfo(String info) {
        Info = info;
    }

    public String getDateFormat() {
        return DateFormat;
    }

    public void setDateFormat(String dateFormat) {
        DateFormat = dateFormat;
    }

    public String getDou() {
        return Dou;
    }

    public void setDou(String dou) {
        Dou = dou;
    }

    public String getHyeonSil() {
        return HyeonSil;
    }

    public void setHyeonSil(String hyeonSil) {
        HyeonSil = hyeonSil;
    }

    public String getsDate() {
        return sDate;
    }

    public void setsDate(String sDate) {
        this.sDate = sDate;
    }

    public String geteDate() {
        return eDate;
    }

    public void seteDate(String eDate) {
        this.eDate = eDate;
    }

    public String getColor() {
        return Color;
    }

    public void setColor(String color) {
        Color = color;
    }

    public String getHyeonSilABC() {
        return HyeonSilABC;
    }

    public void setHyeonSilABC(String hyeonSilABC) {
        HyeonSilABC = hyeonSilABC;
    }
}